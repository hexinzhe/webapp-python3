# 检查是否是 admin
def check_admin(request):
    if request.__user__ is None or not request.__user__.admin:
        raise APIPermissionError()


# 检查请求页数是否合法
def get_page_index(page_str):
    p = 1
    try:
        p = int(page_str)
    except ValueError as e:
        pass
    if p < 1:
        p = 1
    return p


# 将文本转换为 html
def text2html(text):
    lines = map(lambda s: '<p>%s</p>' % s.replace('&', '&amp;').replace('<', '&lt;').replace('>', '&gt;'), filter(lambda s: s.strip() != '', text.split('\n')))
    return ''.join(lines)


def user2cookie(user, max_age):
    '''
    Generate cookie string by: id-expires-sha1
    '''
    # 构建cookie字串
    expires = str(int(time.time() + max_age))
    s = '-'.join([user.id, user.passwd, expires, _COOKIE_KEY])
    # cookie = id-expires-sha1(id-passwd-expires-secret)
    return '-'.join([user.id, expires, hashlib.sha1(s.encode()).hexdigest()])


async def cookie2user(cookie_str):
    '''
    Parse cookie and load user if cookie is valid.
    '''
    if not cookie_str:
        return None
    try:
        L = cookie_str.split('-')
        if len(L) != 3:
            return None
        uid, expires, sha1 = L
        if int(expires) < time.time():
            return None
        user = await User.find(uid)
        if user is None:
            return None
        s = '-'.join([user.id, user.passwd, expires, _COOKIE_KEY])
        if sha1 != hashlib.sha1(s.encode()).hexdigest():
            logging.info('invalid sha1')
            return None
        user.passwd = '******'
        return user
    except Exception as e:
        logging.exception(e)
        return None


